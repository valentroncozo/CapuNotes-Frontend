// src/services/areasService.js
import axios from "axios";
import localStorageApi from "./localStorageApi.js";

// 游녤 Flag para elegir backend real o LocalStorage (por defecto: LocalStorage)
const USE_API = import.meta?.env?.VITE_USE_API === "true";

// Endpoints y claves de storage
const API_URL = "/api/areas";
const AREA_STORAGE_KEY = "areas";

// ===== Implementaci칩n API (axios) =====
const areasServiceApi = {
  // Obtener todas las 치reas
  list: async () => {
    const res = await axios.get(API_URL);
    // Normalizamos a tu shape {id, nombre, descripcion}
    return res.data.map((a) => ({
      id: a.id,
      nombre: a.name,
      descripcion: a.description,
    }));
  },

  // Crear nueva 치rea
  create: async (data) => {
    const payload = {
      name: data.nombre,
      description: data.descripcion,
    };
    const res = await axios.post(API_URL, payload);
    return {
      id: res.data.id,
      nombre: res.data.name,
      descripcion: res.data.description,
    };
  },

  // Editar 치rea existente
  update: async (data) => {
    const payload = {
      name: data.nombre,
      description: data.descripcion,
    };
    const res = await axios.patch(`${API_URL}/${data.id}`, payload);
    return {
      id: res.data.id,
      nombre: res.data.name,
      descripcion: res.data.description,
    };
  },

  // Eliminar 치rea
  remove: async (id) => {
    await axios.delete(`${API_URL}/${id}`);
  },
};

// ===== Implementaci칩n LocalStorage (con validaci칩n de duplicados) =====
const areasServiceLocal = localStorageApi(AREA_STORAGE_KEY, {
  uniqueBy: "nombre",
  messages: {
    createDuplicate: "Ya existe un 치rea con ese nombre.",
    updateDuplicate: "Ya existe otra 치rea con ese nombre.",
  },
});

// ===== Export 칰nico =====
export const areasService = USE_API ? areasServiceApi : areasServiceLocal;

// (Opcional, por si quer칠s referenciarlos expl칤citamente en alg칰n refactor futuro)
export { areasServiceApi, areasServiceLocal };
